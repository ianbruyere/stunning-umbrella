# Here's a guide:
# + designates addition.
add_0 = 3 + 3 # 6
# - designates subtraction.
sub_0 = 6 - 5 # 1
# / is for division
div_0 = 8 / 2 # 4
# * is used for multiplication
mult_0 = 4 * 4 # 16
# the number of operations isn't limited to 1, it can be 2
add_1 = 3 + 7 + 8
# or as many as you like
sub_1 = 245 - 86 - 2 - 26 - 39 # 
# the operations don't have to be of one type
mult_type = 21 + 6 - 3
# and don't worry, negatives are O.K.
neg = 3 - 100 # -97
# paratheses are fair game
para = (10 - (45 + 17)) + 25
# Standard Order of Operations Applies
order = 8 + 4 * (3 + 3) / 6 # 12



